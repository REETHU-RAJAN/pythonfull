# Python program to draw hexagon
# using Turtle Programming
import turtle
x= turtle.Turtle()
num_sides = 6
l = 70
angle = 360.0 / num_sides
x.fillcolor("green")
x.begin_fill()
x.forward(l)
x.left(angle)
x.forward(l)
x.left(angle)
x.forward(l)
x.left(angle)
x.forward(l)
x.left(angle)
x.forward(l)
x.left(angle)
x.forward(l)
x.end_fill()

turtle.done()


